    private void Login1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Login1ActionPerformed
        String username = Username.getText();
        String password = new String(Password.getPassword());

        String role = userControl.authenticate(username, password);
        if (role != null) {
            if (role.equals("ADMIN")) {
                JOptionPane.showMessageDialog(null, "Admin login successful");
                dispose();  // Menutup form login sebelumnya

                // Inisialisasi AdminForm dan menampilkannya
                AdminForm adminForm = new AdminForm(control);
                adminForm.setVisible(true);  // Menampilkan AdminForm
            } else if (role.equals("USER")) {
                JOptionPane.showMessageDialog(null, "User login successful");
                dispose();  // Menutup form login sebelumnya

                // Inisialisasi UserForm dan menampilkannya
                UserForm userForm = new UserForm(userControl, adminControl);
                userForm.setVisible(true);  // Menampilkan UserForm
            } else {
                JOptionPane.showMessageDialog(null, "Unknown role: " + role);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Invalid credentials");
        }
    }//GEN-LAST:event_Login1ActionPerformed



