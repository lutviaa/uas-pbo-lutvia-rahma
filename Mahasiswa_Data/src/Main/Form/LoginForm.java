    private void UsernameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UsernameActionPerformed
        String username = Username.getText().trim(); // Ambil teks dari field Username dan hapus spasi di awal dan akhir

        if (username.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Username cannot be empty", "Empty Username", JOptionPane.ERROR_MESSAGE);
            return; // Kembalikan fokus ke field Username
        }

        // Pindahkan fokus ke field Password saat tombol Enter ditekan
        Password.requestFocusInWindow();
    }//GEN-LAST:event_UsernameActionPerformed

